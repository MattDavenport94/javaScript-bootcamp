// Comparisons
/*
> - greater than
< - less than
>= - greater than or equal to
<= - less than or equal to
== - equality
!= - not equal
=== - strict equality
!== - strict non-equality
*/

console.log(-2 > 1);
console.log(10 < 999);
console.log(55.1 <= 55.5);
console.log("hello".length <= "hello!".length);
